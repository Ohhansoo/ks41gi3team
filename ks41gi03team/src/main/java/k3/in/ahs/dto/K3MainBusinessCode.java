package k3.in.ahs.dto;

public class K3MainBusinessCode {

	
	private String mainBusinessCode;
	private String businessRegistrationDate;
	private String businessDeleteDate;
	
	
	public String getMainBusinessCode() {
		return mainBusinessCode;
	}
	public void setMainBusinessCode(String mainBusinessCode) {
		this.mainBusinessCode = mainBusinessCode;
	}
	public String getBusinessRegistrationDate() {
		return businessRegistrationDate;
	}
	public void setBusinessRegistrationDate(String businessRegistrationDate) {
		this.businessRegistrationDate = businessRegistrationDate;
	}
	public String getBusinessDeleteDate() {
		return businessDeleteDate;
	}
	public void setBusinessDeleteDate(String businessDeleteDate) {
		this.businessDeleteDate = businessDeleteDate;
	}
	
}
